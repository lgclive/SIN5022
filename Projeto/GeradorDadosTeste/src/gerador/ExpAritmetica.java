package gerador;

public class ExpAritmetica {
    private String operandoEsquerda;
    private String operandoDireita;
    private String operador;

    public String getOperandoEsquerda() {
        return operandoEsquerda;
    }

    public void setOperandoEsquerda(String operandoEsquerda) {
        this.operandoEsquerda = operandoEsquerda;
    }

    public String getOperandoDireita() {
        return operandoDireita;
    }

    public void setOperandoDireita(String operandoDireita) {
        this.operandoDireita = operandoDireita;
    }

    public String getOperador() {
        return operador;
    }

    public void setOperador(String operador) {
        this.operador = operador;
    }
}
